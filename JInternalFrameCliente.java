    public void obtenerDatos(){
        
        
        List<Cliente> clientes= new DAOCliente().ObtenerDatos();
        
        DefaultTableModel modelo= new DefaultTableModel();
        
        String[] columnas={"id_cliente","cliente_nombre","cliente_apellido",
            "direccion","numero","correo_electronico","categoria"};
        
        modelo.setColumnIdentifiers(columnas);
        for(Cliente au:clientes){
            
            String[]renglon={Integer.toString(au.getId_cliente()),au.getClient_nombre(),
                au.getClient_apellido(),au.getDireccion(),au.getNum_telefono(),au.getCorreo_elect(),
                au.getCategoria()};
            modelo.addRow(renglon);
        }
        jTableCliente.setModel(modelo);
    }
    
    
    
    
    
    
    
    
    
    

