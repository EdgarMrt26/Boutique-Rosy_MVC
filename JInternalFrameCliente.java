    private void jBAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAñadirActionPerformed
        // TODO add your handling code here:
        
        String nomb=jTextcliente_nombre.getText();
        String apell=jTextcliente_apellido.getText();
        String direc=jTextdireccion.getText();
        String nume=jTextnumero.getText();
        String corr=jTextcorreo_electronico.getText();
        String categ=jTextcategoria.getText();
        
        if(nomb.contentEquals("")||apell.contentEquals("")||
                direc.contentEquals("")||nume.contentEquals("")||
                corr.contentEquals("")||categ.contentEquals("")){
            JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
        }else{
            try{
                Cliente au= new DAOCliente().Insertar(direc, nomb, apell, nume, corr, categ);
                JOptionPane.showMessageDialog(rootPane, "Registro Agregado");
            }catch (Exception e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, "No se agregó el registro");
            }
        }
        obtenerDatos();
        limpiarCampos();
    }//GEN-LAST:event_jBAñadirActionPerformed

    
    
    
    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        // TODO add your handling code here:
        
        
        int fila=this.jTableCliente.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
        }
        else{
            
            
            try{
                int id=Integer.parseInt((String)this.jTableCliente.getValueAt(fila, 0).toString());
                String nom=(String)this.jTableCliente.getValueAt(fila, 1);
                String apell=(String)this.jTableCliente.getValueAt(fila, 2);
                String direc=(String)this.jTableCliente.getValueAt(fila, 3);
                String nume=(String)this.jTableCliente.getValueAt(fila, 4);
                String corr=(String)this.jTableCliente.getValueAt(fila, 5);
                String categ=((String)this.jTableCliente.getValueAt(fila, 6).toString());
                
                
                
                jTextid_cliente.setText(""+id);
                jTextcliente_nombre.setText(nom);
                jTextcliente_apellido.setText(apell);
                jTextdireccion.setText(direc);
                jTextnumero.setText(nume);
                jTextcorreo_electronico.setText(corr);
                jTextcategoria.setText(categ); 
                
            }catch(NumberFormatException e){
                e.printStackTrace();
            }
         
        }
     
        
    }//GEN-LAST:event_jBEditarActionPerformed

    
    public void actualizarCliente(){
        
        
         
        int id=Integer.parseInt(this.jTextid_cliente.getText());
        String nom=this.jTextcliente_nombre.getText();
        String apell=this.jTextcliente_apellido.getText();
        String direc=this.jTextdireccion.getText();
        String nume=this.jTextnumero.getText();
        String corr=this.jTextcorreo_electronico.getText();
        String categ=(this.jTextcategoria.getText());
        
        
        DAOCliente dao=new DAOCliente();
        int res=dao.Actualizar(id, corr, apell, direc, nume, corr, categ);
        if(res==1){
            JOptionPane.showMessageDialog(rootPane, "¡Cliente Actualizado!");
        }
        
        else{
            JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un error!");
        }
        
    }
    
    
    
    
